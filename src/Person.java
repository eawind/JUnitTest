
public class Person {

	private String name;
	private int age;

	public void setName(String string) {
		this.name = string;
		
	}

	public void setAge(int i) {
		this.age = i;
		
	}

	public String getName() {
		return this.name;
	}

	public int getAge() {
		return this.age;
	}

	public void setInfo(String string, int i) {
		this.name = string;
		this.age = i;
		
	}

}
